#include <stdbool.h>

int main() {
    int a;           // integer (32 bits)
    unsigned int b;  // unsigned integer (positive only)
    char c;          // character
    short d;         // short integer (16 bits)
    long e;          // long integer (64 bits)
    float f;         // floating point number (32 bits)
    double g;        // double precision floating point number (64 bits)
    bool h;          // boolean (true or false)

    return 0;
}
